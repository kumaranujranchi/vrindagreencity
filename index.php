    <!-- Newsletter Subscription Script -->
    <script>
        $(document).ready(function () {
            $('#newsletter-form').on('submit', function (e) {
                e.preventDefault();

                var email = $(this).find('input[name="email"]').val();
                var messageDiv = $('#newsletter-message');

                $.ajax({
                    url: 'inc/newsletter.php',
                    type: 'POST',
                    data: { email: email },
                    dataType: 'json',
                    beforeSend: function () {
                        messageDiv.html('<span style="color: #FFD700;">Subscribing...</span>');
                    },
                    success: function (response) {
                        if (response.success) {
                            messageDiv.html('<span style="color: #4CAF50;">✓ ' + response.message + '</span>');
                            $('#newsletter-form')[0].reset();
                        } else {
                            messageDiv.html('<span style="color: #FF6B6B;">✗ ' + response.message + '</span>');
                        }
                    },
                    error: function () {
                        messageDiv.html('<span style="color: #FF6B6B;">✗ Something went wrong. Please try again.</span>');
                    }
                });
            });
        });
    </script>

